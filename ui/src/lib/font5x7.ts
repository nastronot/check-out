function artRowToBits(row: string): number {
  // bit c = column c+1 from the left (bit0 = leftmost).
  let v = 0;
  for (let c = 0; c < CELL_COLS; c++) {
    if (row[c] === '#') v |= 1 << c;
  }
  return v;
}

/** code (0x20..0x7E) -> 7 row values (low 5 bits). */
export const FONT5x7: Record<number, number[]> = (() => {
  const out: Record<number, number[]> = {};
  for (const [ch, art] of Object.entries(FONT_ART)) {
    out[ch.charCodeAt(0)] = art.map(artRowToBits);
  }
  return out;
})();

/** True row data for a character code, honoring user glyphs from state.glyphs. */
export function rowsForCode(code: number, glyphs: GlyphMap): number[] {
  const slot = GLYPH_CODES.indexOf(code);
  if (slot >= 0) {
    const rows = glyphs?.[String(slot)];
    if (Array.isArray(rows) && rows.length === CELL_ROWS) {
      return rows.map((r) => r & 0x1f);
    }
    return EMPTY_ROWS; // declared-but-undefined glyph renders blank
  }
  return FONT5x7[code] ?? FALLBACK_ROWS;
}

/** One row value -> 5 booleans, left to right. */
export function rowToDots(row: number): boolean[] {
  const dots: boolean[] = [];
  for (let c = 0; c < CELL_COLS; c++) dots.push(((row >> c) & 1) === 1);
  return dots;
}

/** A character cell as a 7x5 boolean matrix (rows top→bottom, cols left→right). */
export function cellDots(code: number, glyphs: GlyphMap): boolean[][] {
  return rowsForCode(code, glyphs).map(rowToDots);
}

/** A 20-char line -> 20 cell matrices (short/long lines are padded/clipped). */
export function lineToCells(line: string, glyphs: GlyphMap): boolean[][][] {
  const cells: boolean[][][] = [];
  for (let i = 0; i < LINE_LEN; i++) {
    const code = i < line.length ? line.charCodeAt(i) : 0x20;
    cells.push(cellDots(code, glyphs));
  }
  return cells;
}

/** Count lit dots for a character — used by the preview smoke test. */
export function litCount(code: number, glyphs: GlyphMap = {}): number {
  return cellDots(code, glyphs).reduce(
    (n, row) => n + row.filter(Boolean).length,
    0,
  );
}
