//   a row is a 5-bit value where bit c (0-indexed) is column c+1 from the LEFT
//   (bit0 = leftmost, bit4 = rightmost). The hardware's <<3 wire-encoding lives
//   only in the driver; here, in state.glyphs, and in this font we all speak the
//   same intuitive low-5-bit "pixel" language — so a user glyph and a built-in
//   character render through the identical code path.

